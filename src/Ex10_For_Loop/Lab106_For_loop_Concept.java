package Ex10_For_Loop;

public class Lab106_For_loop_Concept {
    public static void main(String[] args){
        // Concept ;- I      C             U
        for(int pushkar=0; pushkar<10 ; pushkar++){
            if (pushkar == 5){
                System.out.println(pushkar);
            }else{
                System.out.println("Else runned");
            }
        }
        System.out.println("END");
    }
}

