package Ex10_For_Loop;

public class Lab103_For_loop_with_if_else {
    public static void main(String[] args){

        for(int pushkar=0; pushkar<10 ; pushkar++){
            if (pushkar == 5){
                System.out.println(pushkar);
            }else{
                System.out.println("Else runned");
            }
        }
        System.out.println("END");
    }
}

