package Ex10_For_Loop;

public class Lab108_For_loop3 {
    public static void main(String[] args){
        // Concept ;- I      C             U
        for(long i=-1; i>-10 ; i--){
            System.out.println(i);

        }
        int i = 10; // we can have initialization outside of the loop
        for(; i<20; i++){
            System.out.println(i);
        }

    }
}
