package Ex10_For_Loop;

public class Lab107_For_loop2 {
    public static void main(String[] args){
        // Concept ;- I      C             U
        for(long pushkar=0; pushkar<10 ; pushkar++){
            System.out.println(pushkar);

        }
        for(float pushkar=0; pushkar<10 ; pushkar++){
            System.out.println(pushkar);

        }
        System.out.println("END");
    }
}
