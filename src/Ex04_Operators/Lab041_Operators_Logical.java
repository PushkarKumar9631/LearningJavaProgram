package Ex04_Operators;

public class Lab041_Operators_Logical {
    public static void main(String[] args) {

        boolean a = true;
        System.out.println(!a); // Not equal

        boolean b = true;
        System.out.println(!!b); //not of not is true

        boolean c= true || false;  // OR Operator

        System.out.println(c);

        boolean c1= false && true; // AND Operator
        System.out.println(c1);




    }
}