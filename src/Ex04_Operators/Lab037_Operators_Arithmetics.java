package Ex04_Operators;

public class Lab037_Operators_Arithmetics {
    public static void main(String[] args){
        // Arithmetic operator
        // + (Addition)
        // - (Substraction)
        // * (Multiplication)
        // / (Division)
        // % (Modulus) | Modulus or reminder

        // it always requires two operands to perform any action

        int a = 20;
        int b = 3;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c); // rule: if we divide anything by a float number, the output will always be a float
        System.out.println("a+b");
        System.out.println(a+b);
    }
}
